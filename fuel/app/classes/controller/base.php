/**
 * The Base Controller.
 *
 * Controller for base stuff such as 404
 *
 * @package  app
 * @extends  Controller
 */
class Controller_Base extends Controller
{

	/**
	 * The 404 action for the application.
	 *
	 * @access  public
	 * @return  Response
	 */
	public function action_404()
	{
		return Response::forge(Presenter::forge('404'), 404);
	}
}
